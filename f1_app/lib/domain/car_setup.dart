class CarSetup {
  final String engine;
  final int frontWingAngle;
  final int rearWingAngle;
  final String tyreCompound;
  final double fuelLoad;

  const CarSetup({
    required this.engine,
    required this.frontWingAngle,
    required this.rearWingAngle,
    required this.tyreCompound,
    required this.fuelLoad,
  });
}
